def bootstrap_metric(y_true, y_score, metric_fn, n_iterations=1000, random_state=42):
    """
    Calcula intervalo de confianza al 95% usando bootstrap.

    Parameters
    ----------
    y_true : array-like
        Etiquetas reales.

    y_score : array-like
        Predicciones o probabilidades.

    metric_fn : callable
        Función de métrica. Ejemplo: roc_auc_score, f1_score.

    Returns
    -------
    ci : array
        Percentiles [2.5, 97.5].
    """

    import numpy as np

    rng = np.random.RandomState(random_state)

    y_true_arr = np.array(y_true)
    y_score_arr = np.array(y_score)

    scores = []

    for _ in range(n_iterations):
        idx = rng.randint(0, len(y_true_arr), len(y_true_arr))

        if len(np.unique(y_true_arr[idx])) < 2:
            continue

        score = metric_fn(y_true_arr[idx], y_score_arr[idx])
        scores.append(score)

    return np.percentile(scores, [2.5, 97.5])