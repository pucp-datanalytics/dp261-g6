def validate_many_models(model_paths, X_sample):
    """
    Valida varios modelos persistidos.

    Parameters
    ----------
    model_paths : dict
        Ejemplo:
        {
            "tuned_rf": "../models/tuned_rf.pkl",
            "final_model": "../models/final_model.pkl"
        }
    """

    results = {}

    for name, path in model_paths.items():
        model = load_model(path)
        preds = model.predict(X_sample)
        results[name] = preds

    return results


