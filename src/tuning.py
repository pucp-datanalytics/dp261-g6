# 2. TUNING CON GRIDSEARCHCV
# ============================================================

def tune_model_grid(
    pipe,
    param_grid,
    X,
    y,
    cv,
    scoring="f1",
    name="model",
    models_dir="../models",
    log_path="../models/experiments_log.csv",
    n_jobs=-1,
    verbose=1,
    notes="Tuning con GridSearchCV"
):
    """
    Ejecuta GridSearchCV, guarda el mejor modelo y registra el experimento.

    Parameters
    ----------
    pipe : sklearn Pipeline o estimator
        Modelo o pipeline a tunear.

    param_grid : dict
        Espacio de búsqueda para GridSearchCV.

    X, y :
        Datos de entrenamiento.

    cv :
        Estrategia de validación cruzada.

    scoring : str
        Métrica principal. Ejemplo: "f1", "recall", "roc_auc".

    name : str
        Nombre corto del modelo. Ejemplo: "rf", "lr", "xgb".

    Returns
    -------
    grid : GridSearchCV
        Objeto entrenado.

    result : dict
        Registro del experimento.
    """

    ensure_dir(models_dir)

    start = time.time()

    grid = GridSearchCV(
        estimator=pipe,
        param_grid=param_grid,
        scoring=scoring,
        cv=cv,
        n_jobs=n_jobs,
        verbose=verbose,
        return_train_score=True
    )

    grid.fit(X, y)

    elapsed = round(time.time() - start, 2)

    model_path = os.path.join(models_dir, f"tuned_{name}.pkl")
    save_model(grid.best_estimator_, model_path)

    result = {
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "sprint": "Sprint 4",
        "stage": "PB-13 Hyperparameter Tuning",
        "model": name,
        "experiment_type": "GridSearchCV",
        "scoring": scoring,
        "best_score": grid.best_score_,
        "best_params": json.dumps(grid.best_params_, ensure_ascii=False),
        "time_seconds": elapsed,
        "model_path": model_path,
        "selected": False,
        "notes": notes
    }

    append_experiment(result, log_path)

    return grid, result


