def validate_model_load_and_predict(model_path, X_sample):
    """
    Valida que un modelo guardado se puede cargar y predecir.
    """

    model = load_model(model_path)
    preds = model.predict(X_sample)

    return preds


def validate_saved_models(model_paths, X_sample):
    """
    Valida varios modelos guardados.

    Parameters
    ----------
    model_paths : dict
        Ejemplo:
        {
            "rf": "../models/baseline_rf.pkl",
            "final_model": "../models/final_model.pkl"
        }

    X_sample : pandas.DataFrame
        Muestra de prueba.

    Returns
    -------
    validation_results : dict
        Predicciones por modelo.
    """

    validation_results = {}

    for name, path in model_paths.items():
        model = load_model(path)
        preds = model.predict(X_sample)
        validation_results[name] = preds

    return validation_results


