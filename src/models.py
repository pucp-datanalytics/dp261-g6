# 4. Evaluación sobre test set
# ============================================================

def evaluate_on_test(model, X_test, y_test, positive_label=1):
    """
    Evalúa un modelo ya entrenado sobre el conjunto de test.

    Parameters
    ----------
    model : sklearn Pipeline o estimator
        Modelo entrenado.
    X_test : pandas.DataFrame
        Variables predictoras de test.
    y_test : pandas.Series
        Target real.
    positive_label : int or str
        Clase positiva.

    Returns
    -------
    metrics : dict
        Métricas principales.
    report : dict
        Classification report.
    cm : array
        Matriz de confusión.
    """

    y_pred = model.predict(X_test)

    metrics = {
        "accuracy": accuracy_score(y_test, y_pred),
        "precision": precision_score(
            y_test,
            y_pred,
            pos_label=positive_label,
            zero_division=0
        ),
        "recall": recall_score(
            y_test,
            y_pred,
            pos_label=positive_label,
            zero_division=0
        ),
        "f1": f1_score(
            y_test,
            y_pred,
            pos_label=positive_label,
            zero_division=0
        )
    }

    if hasattr(model, "predict_proba"):
        y_proba = model.predict_proba(X_test)[:, 1]
        metrics["roc_auc"] = roc_auc_score(y_test, y_proba)
    else:
        y_proba = None
        metrics["roc_auc"] = None

    report = classification_report(
        y_test,
        y_pred,
        output_dict=True,
        zero_division=0
    )

    cm = confusion_matrix(y_test, y_pred)

    return metrics, report, cm


