import os
import json
import joblib
import pandas as pd

from datetime import datetime

from sklearn.model_selection import cross_validate
from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    roc_auc_score,
    classification_report,
    confusion_matrix
)


# ============================================================
# 1. Utilidades generales
# ============================================================

def ensure_dir(path):
    """
    Crea un directorio si no existe.
    """
    if path:
        os.makedirs(path, exist_ok=True)


def save_model(model, model_path):
    """
    Guarda un modelo o pipeline con joblib.
    """
    ensure_dir(os.path.dirname(model_path))
    joblib.dump(model, model_path)
    return model_path


def load_model(model_path):
    """
    Carga un modelo o pipeline guardado con joblib.
    """
    return joblib.load(model_path)


# ============================================================
# 2. Entrenamiento y persistencia de modelos baseline
# ============================================================

def train_baseline(pipe, X, y, name, models_dir="../models"):
    """
    Entrena y guarda un modelo baseline.

    Parameters
    ----------
    pipe : sklearn Pipeline o estimator
        Modelo o pipeline completo.
    X : pandas.DataFrame
        Variables predictoras.
    y : pandas.Series
        Variable objetivo.
    name : str
        Nombre corto del modelo. Ejemplo: "lr", "rf", "xgb".
    models_dir : str
        Carpeta donde se guardará el modelo.

    Returns
    -------
    pipe : estimator
        Modelo entrenado.
    model_path : str
        Ruta del modelo guardado.
    """
    ensure_dir(models_dir)

    pipe.fit(X, y)

    model_path = os.path.join(models_dir, f"baseline_{name}.pkl")
    joblib.dump(pipe, model_path)

    return pipe, model_path


