# 6. Flujo completo: entrenar, evaluar y registrar
# ============================================================

def train_evaluate_log(
    pipe,
    X,
    y,
    name,
    params,
    cv,
    scoring,
    models_dir="../models",
    log_path="../models/experiments_log.csv",
    selected=False,
    notes="",
    sprint="Sprint 3",
    stage="Baseline Modeling",
    experiment_type="baseline"
):
    """
    Ejecuta el flujo completo:

    1. Entrena modelo.
    2. Guarda modelo con joblib.
    3. Evalúa con cross-validation.
    4. Registra en experiments_log.csv.
    """

    fitted_pipe, model_path = train_baseline(
        pipe=pipe,
        X=X,
        y=y,
        name=name,
        models_dir=models_dir
    )

    _, metrics_summary = evaluate_model(
        pipe=fitted_pipe,
        X=X,
        y=y,
        cv=cv,
        scoring=scoring
    )

    log_experiment(
        name=name,
        params=params,
        metrics=metrics_summary,
        path=log_path,
        model_path=model_path,
        selected=selected,
        notes=notes,
        sprint=sprint,
        stage=stage,
        experiment_type=experiment_type
    )

    return fitted_pipe, metrics_summary, model_path


