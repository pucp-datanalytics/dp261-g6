# 5. Registro de experimentos
# ============================================================

def log_experiment(
    name,
    params,
    metrics,
    path="../models/experiments_log.csv",
    model_path=None,
    selected=False,
    notes="",
    sprint="Sprint 3",
    stage="Baseline Modeling",
    experiment_type="baseline"
):
    """
    Registra un experimento en experiments_log.csv.

    Parameters
    ----------
    name : str
        Nombre del modelo.
    params : dict
        Parámetros del modelo o experimento.
    metrics : dict
        Métricas obtenidas.
    path : str
        Ruta del log.
    model_path : str
        Ruta del modelo persistido.
    selected : bool
        Indica si fue seleccionado como candidato.
    notes : str
        Comentarios.
    sprint : str
        Sprint asociado.
    stage : str
        Etapa o PB asociado.
    experiment_type : str
        Tipo de experimento.
    """

    ensure_dir(os.path.dirname(path))

    row = {
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "sprint": sprint,
        "stage": stage,
        "model": name,
        "experiment_type": experiment_type,
        "params": json.dumps(params, ensure_ascii=False),
        "model_path": model_path,
        "selected": selected,
        "notes": notes,
        **metrics
    }

    df_row = pd.DataFrame([row])

    if os.path.exists(path):
        df_row.to_csv(path, mode="a", header=False, index=False)
    else:
        df_row.to_csv(path, index=False)

    return row


